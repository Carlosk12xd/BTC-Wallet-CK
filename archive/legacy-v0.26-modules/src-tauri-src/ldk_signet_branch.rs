pub fn start_plan(input: LdkSignetRuntimeStartInput) -> LdkSignetRuntimeStartPlan {
    let mut blockers = Vec::new();
    let network = input.network.trim().to_lowercase();

    if network == "mainnet" || network == "bitcoin" {
        blockers.push("Mainnet requested. v0.13 only permits signet/testnet planning.".to_string());
    }
    if !input.confirmed_signet_only {
        blockers.push("Confirm that this is signet/testnet only.".to_string());
    }
    if !input.backup_confirmed {
        blockers.push("Confirm that recovery words / node secret backup plan exists.".to_string());
    }
    if !input.restore_test_confirmed {
        blockers.push("Confirm that restore will be tested before any mainnet release.".to_string());
    }
    if !input.inbound_liquidity_plan_confirmed {
        blockers.push("Confirm inbound liquidity/channel plan for receiving OCEAN payouts.".to_string());
    }
    if input.data_dir.trim().is_empty() {
        blockers.push("Set an isolated signet/testnet data directory.".to_string());
    }
    if input.esplora_url.trim().is_empty() {
        blockers.push("Set a signet/testnet Esplora URL or local chain source.".to_string());
    }

    let safe = blockers.is_empty();

    LdkSignetRuntimeStartPlan {
        safe_to_attempt_local_feature_build: safe,
        network: if network.is_empty() { "signet".to_string() } else { network },
        alias: if input.alias.trim().is_empty() { "CarlosK-Signet".to_string() } else { input.alias },
        data_dir: input.data_dir,
        cargo_commands: vec![
            "git checkout -b feature/ldk-node-signet-runtime".to_string(),
            "cd src-tauri".to_string(),
            "cargo check".to_string(),
            "cargo check --features ldk-node-signet-runtime".to_string(),
            "RUST_LOG=info cargo test --features ldk-node-signet-runtime".to_string(),
        ],
        runtime_sequence: vec![
            "Build ldk_node::Builder with signet/testnet network only.".to_string(),
            "Set wallet/node storage path to the isolated CarlosK signet data directory.".to_string(),
            "Configure Esplora chain source and rapid gossip sync source.".to_string(),
            "Start the node and persist runtime status.".to_string(),
            "Create a signet/testnet on-chain funding address.".to_string(),
            "Open or request inbound liquidity on signet/testnet.".to_string(),
            "Generate a test BOLT12 offer for a small receive test.".to_string(),
        ],
        pseudo_code: vec![
            "let mut builder = ldk_node::Builder::new();".to_string(),
            "builder.set_network(Network::Signet);".to_string(),
            "builder.set_storage_dir_path(data_dir);".to_string(),
            "builder.set_chain_source_esplora(esplora_url, None);".to_string(),
            "let node = builder.build()?;".to_string(),
            "node.start()?;".to_string(),
            "let offer = node.bolt12_payment().receive(...)?;".to_string(),
        ],
        blockers,
        acceptance_tests: vec![
            "Default build still compiles without ldk-node.".to_string(),
            "Feature build compiles with ldk-node on developer machine.".to_string(),
            "Signet node starts and stops cleanly.".to_string(),
            "Generated test offer starts with lno1 and is clearly labeled signet/testnet.".to_string(),
            "Mainnet requests are rejected in v0.13 and v0.13 test builds.".to_string(),
        ],
        warning: "This is a signet/testnet branch plan. Do not connect real OCEAN payouts or real BTC until mainnet release gates are explicitly implemented and tested.".to_string(),
    }
}

