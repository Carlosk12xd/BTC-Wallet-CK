use serde::{Deserialize, Serialize};

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct LdkSignetBranchStatus {
    pub version: String,
    pub branch_name: String,
    pub cargo_feature: String,
    pub optional_dependency: String,
    pub dependency_wired_in_cargo: bool,
    pub default_build_uses_ldk: bool,
    pub compiled_with_feature: bool,
    pub implemented_now: Vec<String>,
    pub next_local_steps: Vec<String>,
    pub mainnet_guardrails: Vec<String>,
    pub warning: String,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct LdkSignetRuntimeStartInput {
    pub alias: String,
    pub network: String,
    pub data_dir: String,
    pub esplora_url: String,
    pub rgs_url: String,
    pub confirmed_signet_only: bool,
    pub backup_confirmed: bool,
    pub restore_test_confirmed: bool,
    pub inbound_liquidity_plan_confirmed: bool,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct LdkSignetRuntimeStartPlan {
    pub safe_to_attempt_local_feature_build: bool,
    pub network: String,
    pub alias: String,
    pub data_dir: String,
    pub cargo_commands: Vec<String>,
    pub runtime_sequence: Vec<String>,
    pub pseudo_code: Vec<String>,
    pub blockers: Vec<String>,
    pub acceptance_tests: Vec<String>,
    pub warning: String,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct LdkSignetOfferRecipe {
    pub goal: String,
    pub prerequisites: Vec<String>,
    pub ldk_api_targets: Vec<String>,
    pub expected_outputs: Vec<String>,
    pub non_goals: Vec<String>,
    pub warning: String,
}

