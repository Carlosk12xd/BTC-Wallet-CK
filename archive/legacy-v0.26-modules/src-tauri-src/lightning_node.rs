pub fn receive_readiness(has_saved_offer: bool, has_backup_confirmed: bool, pairing_matches: bool) -> LightningReceiveReadiness {
    let mut checks = vec![];
    let mut missing = vec![];

    if has_saved_offer { checks.push("BOLT12 offer saved".to_string()); } else { missing.push("Save a BOLT12 offer".to_string()); }
    if has_backup_confirmed { checks.push("Mining wallet backup confirmed".to_string()); } else { missing.push("Confirm mining wallet backup".to_string()); }
    if pairing_matches { checks.push("OCEAN JSON offer matches saved BOLT12 offer".to_string()); } else { missing.push("Verify OCEAN JSON offer matches saved BOLT12 offer".to_string()); }

    let ready = has_saved_offer && has_backup_confirmed && pairing_matches;
    let recommendation = if ready {
        "Ready to sign the OCEAN payout message, assuming the mining address shown is yours.".to_string()
    } else {
        "Do not sign yet. Complete the missing checks first.".to_string()
    };

    LightningReceiveReadiness {
        ready_for_ocean_mainnet: ready,
        checks,
        missing,
        recommendation,
    }
}

