#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct LightningReceiveReadiness {
    pub ready_for_ocean_mainnet: bool,
    pub checks: Vec<String>,
    pub missing: Vec<String>,
    pub recommendation: String,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
