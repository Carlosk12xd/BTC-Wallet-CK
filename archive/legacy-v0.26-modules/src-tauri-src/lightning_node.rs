pub fn start_signet_node_preview(config: SignetNodeConfig) -> SignetNodeStatus {
    let offer_hint = format!(
        "lno1-signet-placeholder-{}-do-not-use-for-ocean-mainnet",
        config.alias.to_lowercase().replace(|c: char| !c.is_ascii_alphanumeric(), "")
    );

    SignetNodeStatus {
        phase: "v0.10 signet LDK Node preview".to_string(),
        network: config.network,
        alias: config.alias,
        data_dir: config.data_dir,
        offer_generation_available: false,
        simulated_offer: offer_hint,
        implemented_now: vec![
            "Lightning node configuration model".to_string(),
            "Signet-only startup preview command".to_string(),
            "Mainnet receiving lockout in UI and backend status".to_string(),
            "Placeholder offer string clearly marked as non-payment data".to_string(),
        ],
        blocked_by: vec![
            "ldk-node is not linked in the default build yet".to_string(),
            "Real BOLT12 generation must be tested on signet first".to_string(),
            "Channel state backup and recovery are not implemented".to_string(),
            "Inbound liquidity management is not implemented".to_string(),
        ],
        next_manual_test_steps: vec![
            "Create an ldk-node-experimental branch".to_string(),
            "Pin ldk-node and related Lightning crates".to_string(),
            "Replace this preview with real NodeBuilder initialization on signet".to_string(),
            "Generate a real signet BOLT12 offer and verify it parses".to_string(),
            "Only after recovery tests pass, design the mainnet migration switch".to_string(),
        ],
        warning: "This is not a real Lightning node yet and the simulated offer must not be pasted into OCEAN. v0.10 is a safe developer checkpoint for signet/testnet integration.".to_string(),
    }
}

